import torch
import torch.nn as nn
import torch.nn.functional as F
import math

values_per_item = 4

def pack_weights(quantized_weights: torch.Tensor) -> torch.Tensor:
    original_shape = quantized_weights.shape
    row_dim = (original_shape[0] + values_per_item - 1) // values_per_item
    packed_tensor_shape = (row_dim,) if len(original_shape) == 1 else (row_dim, *original_shape[1:])

    quantized_weights = quantized_weights.clone() + 1
    packed = torch.zeros(packed_tensor_shape, device=quantized_weights.device, dtype=torch.uint8)
    unpacked = quantized_weights.to(torch.uint8)

    it = min(values_per_item, (original_shape[0] // row_dim) + 1)
    for i in range(it):
        start = i * row_dim
        end = min(start + row_dim, original_shape[0])
        packed[: (end - start)] |= unpacked[start:end] << (2 * i)
    return packed

def unpack_weights(packed: torch.Tensor, dtype: torch.dtype) -> torch.Tensor:
    packed_shape = packed.shape
    original_row_dim = packed_shape[0] * values_per_item
    unpacked_shape = (original_row_dim,) if len(packed_shape) == 1 else (original_row_dim, *packed_shape[1:])

    unpacked = torch.zeros(unpacked_shape, device=packed.device, dtype=torch.uint8)
    for i in range(values_per_item):
        start = i * packed_shape[0]
        end = start + packed_shape[0]
        mask = 3 << (2 * i)
        unpacked[start:end] = (packed & mask) >> (2 * i)
    return unpacked.to(dtype) - 1

class BitNetLoraLinear(nn.Module):
    def __init__(self, in_features, out_features, bias=False, r=8, lora_alpha=16, target_dtype=torch.float32):
        super().__init__()
        self.in_features = in_features
        self.out_features = out_features
        self.target_dtype = target_dtype
        self.r = r
        self.scaling = lora_alpha / r

        packed_out_features = (out_features + values_per_item - 1) // values_per_item
        self.register_buffer("packed_base_weight", torch.zeros((packed_out_features, in_features), dtype=torch.uint8))
        self.register_buffer("weight_scale", torch.ones((1, 1), dtype=target_dtype))

        self.bias = nn.Parameter(torch.zeros((out_features,), dtype=target_dtype)) if bias else None
        self.lora_A = nn.Parameter(torch.zeros((r, in_features), dtype=target_dtype))
        self.lora_B = nn.Parameter(torch.zeros((out_features, r), dtype=target_dtype))

        nn.init.kaiming_uniform_(self.lora_A, a=math.sqrt(5))
        nn.init.zeros_(self.lora_B)

    def forward(self, x):
        input_dtype = x.dtype
        x = x.to(self.target_dtype)

        base_weight = unpack_weights(self.packed_base_weight, dtype=self.target_dtype)[:self.out_features, :]
        base_output = F.linear(x, base_weight * self.weight_scale, self.bias)

        lora_output = F.linear(F.linear(x, self.lora_A), self.lora_B) * self.scaling
        return (base_output + lora_output).to(input_dtype)

def convert_to_bitnet_lora(model, r=8, lora_alpha=16):
    for name, module in list(model.named_modules()):
        if isinstance(module, nn.Linear) and not any(ex in name for ex in ["embed_tokens", "lm_head"]):
            device = module.weight.device

            orig_w = module.weight.data
            scale = orig_w.abs().mean().clamp(min=1e-5)
            ternary_w = (orig_w / scale).round().clamp(-1, 1)

            new_layer = BitNetLoraLinear(module.in_features, module.out_features, module.bias is not None, r, lora_alpha, torch.float32).to(device)

            with torch.no_grad():
                new_layer.packed_base_weight.copy_(pack_weights(ternary_w))
                new_layer.weight_scale.copy_(scale.to(torch.float32))
                if module.bias is not None:
                    new_layer.bias.copy_(module.bias.data.to(torch.float32))

            model.set_submodule(name, new_layer)
    return model


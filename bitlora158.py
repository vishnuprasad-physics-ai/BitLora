def inspect_bitnet_quantization(model):

    total_layers_checked = 0
    quantized_layers_count = 0

    for name, module in model.named_modules():
        if module.__class__.__name__ == "BitNetLoraLinear":
            total_layers_checked += 1

            packed_weight = module.packed_base_weight
            weight_scale = module.weight_scale

            packed_shape = list(packed_weight.shape)
            unpacked_out_features = module.out_features
            in_features = module.in_features

            raw_sample = packed_weight[:2, :3].tolist()

            from __main__ import unpack_weights
            unpacked_weight_sample = unpack_weights(packed_weight[:1, :], dtype=torch.float32)
            ternary_values_sample = unpacked_weight_sample[0, :8].tolist()

            if quantized_layers_count < 3:
                print(f"Layer: {name}")
                print(f"Storage Data Type : {packed_weight.dtype}")
                print(f"Expected Linear Dimensions : [{unpacked_out_features} x {in_features}]")
                print(f"Actual Packed Storage Shape: {packed_shape}")
                print(f"Raw Memory Values Sample: {raw_sample}")
                print(f"Unpacked Ternary Values: {ternary_values_sample} (Should only be -1.0, 0.0, or 1.0)")
                print(f"Quantization Scale Factor: {weight_scale.item():.6f}")
                print("-" * 50)

            quantized_layers_count += 1

    print(f"\nSummary:")
    print(f"Successfully located and verified {quantized_layers_count} out of {total_layers_checked} BitNet layers.")
    if quantized_layers_count > 0:
        print("Weights are quantized")
    else:
         print("No packed BitNet layers found")

inspect_bitnet_quantization(model)


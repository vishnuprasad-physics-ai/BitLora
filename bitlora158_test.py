def calculate_perplexity(model, tokenizer, dataset_name="roneneldan/TinyStories", split="validation[:100]"):
    print(f"loading benchmark dataset: {dataset_name} ({split})...")
    test_ds = load_dataset(dataset_name, split=split)

    model.eval()
    nlls = []
    max_length = 256

    for entry in tqdm(test_ds):
        encodings = tokenizer(entry["text"], return_tensors="pt", truncation=True, max_length=max_length).to("cuda")
        target_ids = encodings.input_ids.clone()

        with torch.no_grad():
            outputs = model(encodings.input_ids, labels=target_ids)
            neg_log_likelihood = outputs.loss

        nlls.append(neg_log_likelihood)

    # calculate perplexity
    ppl = torch.exp(torch.stack(nlls).mean())
    return ppl.item()

def run_lambada_accuracy(model, tokenizer, num_samples=100):
    dataset = load_dataset("cimec/lambada", split="test", trust_remote_code=True).select(range(num_samples))
    correct = 0

    for sample in dataset:
        text = sample["text"]
        words = text.split()
        context = " ".join(words[:-1])
        target_word = words[-1]

        inputs = tokenizer(context, return_tensors="pt").to("cuda")
        with torch.no_grad():
            outputs = model(**inputs)
            next_token_logits = outputs.logits[0, -1, :]
            predicted_token_id = torch.argmax(next_token_logits).item()
            predicted_word = tokenizer.decode(predicted_token_id).strip()

        if predicted_word.lower() == target_word.lower():
            correct += 1

    accuracy = (correct / num_samples) * 100
    print(f"\nBenchmark Result - Next-word Accuracy: {accuracy:.2f}%")

